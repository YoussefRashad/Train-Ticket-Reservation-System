    private void jLabel18MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel18MouseClicked
        // TODO add your handling code here:
        
        jPanelWrongIDTicket.setVisible(false);
        if(!jTextFieldSearchTicket.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchTicket.getText())){
            Ticket t = new Ticket();
            Ticket x=t.getTicket(Integer.parseInt(jTextFieldSearchTicket.getText()));
            if(x!=null){
                x.SetLine(jComboBoxNewLine.getSelectedItem().toString());
                x.SelectSource(jComboBoxNewSource.getSelectedItem().toString());
                x.SelectDistantion(jComboBoxNewDistination.getSelectedItem().toString());
                jTextFieldNewSalary.setText(""+(x.PriceOFTicket(x.getLine(),x.GetSource(),x.GetDistantion())));
                x.SetPrice(Integer.parseInt(jTextFieldNewSalary.getText()));
                if(x.EditTicketServed(x.GetIDTicket())){
                    jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                    resetPanel();
                }
                else
                    jLabelSuccessOrFail.setText("Failed Updated ...! :(");
            }
            else{
                    jLabelSuccessOrFail.setText("Not Found ...! :(");
                    JOptionPane.showMessageDialog(null, "Not Found ID Ticket ...!\n Try Again :(");
                    resetPanel();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Ticket Try Again :(\nHint: ID Ticket Length should more than 4 .. ;)");
                jPanelWrongIDTicket.setVisible(true);
            }
        }
        else
            jLabelSuccessOrFail.setText("Missing Required ID Ticket ... ! Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel18MouseClicked

