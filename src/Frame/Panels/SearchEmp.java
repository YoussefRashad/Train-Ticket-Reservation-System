    private void jLabelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelSearchMouseClicked
        // TODO add your handling code here:
        jPanelWrongAge.setVisible(false);
        jPanelWrongEmail.setVisible(false);
        jPanelWrongID.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongPass.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        jPanelWrongQualification.setVisible(false);
        jPanelWrongSalary.setVisible(false);
        jPanelWrongUserName.setVisible(false);
        jPanelNotAllowed.setVisible(false);
        jPanelWrongIDEmployee.setVisible(false);
        
        if(!jTextFieldSearchIDEmployee.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchIDEmployee.getText())){
                Employee emp = new Employee();
                Employee x = emp.getEmp(Integer.parseInt(jTextFieldSearchIDEmployee.getText()));
                if(x!=null && x.GetID()>0){
                    setPanel(x);
                    jLabelSuccessOrFail.setText("Founded ...! :) ");

                }
                else{
                    jLabelSuccessOrFail.setText("Not Found ...! :(");
                    JOptionPane.showMessageDialog(null, "Not Found ID Employee ...!\n Try Again :(");
                    ResetPanel();
                }
            }
            else{
                jLabelSuccessOrFail.setText("Not valid ID Search for Employee :( ");
                JOptionPane.showMessageDialog(null, "Not valid ID Employee Try Again :(\nHint: ID Employee Length should more than 4 .. ;)");
                jPanelWrongIDEmployee.setVisible(true);
            }
        }
        else{
            jLabelSuccessOrFail.setText("Missing Required Fields ... ! Please Fill All Fields :(");
            ResetPanel();
        }
    }//GEN-LAST:event_jLabelSearchMouseClicked
