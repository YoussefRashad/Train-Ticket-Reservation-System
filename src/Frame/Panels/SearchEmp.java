    private void jLabel18MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel18MouseClicked
        // TODO add your handling code here:
        jPanelWrongAge.setVisible(false);
        jPanelWrongEmail.setVisible(false);
        jPanelWrongID.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongPass.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        jPanelWrongQualification.setVisible(false);
        jPanelWrongSalary.setVisible(false);
        jPanelWrongUserName.setVisible(false);
        jPanelWrongIDEmployee.setVisible(false);
        jPanelNotAllowed.setVisible(false);
        
        if(!jTextFieldID.getText().equals("") && !jTextFieldName.getText().equals("") && !jTextFieldPass.getText().equals("") && !jTextFieldQualification.getText().equals("") && !jTextFieldSalary.getText().equals("") && !jTextFieldUserName.getText().equals("") && !jTextFieldEmail.getText().equals("") && !jTextFieldAge.getText().equals("")  && !jTextFieldSalary.getText().equals("") )
        {
            //All Check ...
            Validation v = new Validation();
                if(v.checkID(jTextFieldSearchIDEmployee.getText())){
                if(v.checkID(jTextFieldID.getText())){
                    if(v.checkName(jTextFieldName.getText())){
                        if(v.checkName(jTextFieldUserName.getText())){
                            if(v.checkPass(jTextFieldPass.getText())){
                                    if(v.checkEmail(jTextFieldEmail.getText())){
                                        if(v.checkAge(jTextFieldAge.getText())){
                                            if(v.checkPhoneNo(jTextFieldPhoneNo.getText())){
                                                if(v.checkSalary(jTextFieldSalary.getText())){
                                                    if(v.checkName(jTextFieldQualification.getText())){
                                                        // End Check :)
                                                        // Start Add
                                                        Employee x = new Employee();
                                                        if(!jTextFieldID.getText().equals(jTextFieldSearchIDEmployee.getText())){
                                                            if(!x.Dublicate(Integer.parseInt(jTextFieldID.getText()))){
                                                                x.SetAge(Integer.parseInt(jTextFieldAge.getText()));
                                                                x.SetEmail(jTextFieldEmail.getText());
                                                                x.SetGender(jComboBoxGender.getSelectedItem().toString());
                                                                x.SetID(Integer.parseInt(jTextFieldID.getText()));
                                                                x.SetName(jTextFieldName.getText());
                                                                x.SetPassword(jTextFieldPass.getText());
                                                                x.SetPhoneNo(jTextFieldPhoneNo.getText());
                                                                x.SetQualification(jTextFieldQualification.getText());
                                                                x.SetSalary(Double.parseDouble(jTextFieldSalary.getText()));
                                                                x.SetUsername(jTextFieldUserName.getText());
                                                                    if(x.EditInEmployee(Integer.parseInt(jTextFieldSearchIDEmployee.getText()))){
                                                                        ResetPanel();
                                                                        jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                                                    }
                                                                    else{
                                                                        jLabelSuccessOrFail.setText("Failed Updated ...! :("); 
                                                                        JOptionPane.showMessageDialog(null, "Failed Updated ...! :(");
                                                                        ResetPanel();
                                                                    }
                                                                    //End Start & start elses for Check
                                                                 }
                                                            else{
                                                                    jLabelSuccessOrFail.setText("Not Allowed New ID Employee :(");
                                                                    jPanelNotAllowed.setVisible(true);
                                                                    JOptionPane.showMessageDialog(null, "Not Allowed ID Employee , ID Employee is Reserved...!\n Try Again :(");
                                                            }
                                                        }
                                                        else{
                                                            x.SetAge(Integer.parseInt(jTextFieldAge.getText()));
                                                                x.SetEmail(jTextFieldEmail.getText());
                                                                x.SetGender(jComboBoxGender.getSelectedItem().toString());
                                                                x.SetID(Integer.parseInt(jTextFieldID.getText()));
                                                                x.SetName(jTextFieldName.getText());
                                                                x.SetPassword(jTextFieldPass.getText());
                                                                x.SetPhoneNo(jTextFieldPhoneNo.getText());
                                                                x.SetQualification(jTextFieldQualification.getText());
                                                                x.SetSalary(Double.parseDouble(jTextFieldSalary.getText()));
                                                                x.SetUsername(jTextFieldUserName.getText());
                                                                    if(x.EditInEmployee(Integer.parseInt(jTextFieldSearchIDEmployee.getText()))){
                                                                        ResetPanel();
                                                                        jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                                                    }
                                                                    else{
                                                                        jLabelSuccessOrFail.setText("Failed Updated ...! :("); 
                                                                        JOptionPane.showMessageDialog(null, "Failed Updated ...! :(");
                                                                        ResetPanel();
                                                                    }
                                                                    //End Start & start elses for Check
                                                        }
                                                }
                                                else{
                                                        JOptionPane.showMessageDialog(null, "Not valid Input for Qualification Try Again :(");
                                                        jPanelWrongQualification.setVisible(true);
                                                }
                                            }
                                                else{
                                                    JOptionPane.showMessageDialog(null, "Not valid Salary Try Again :(\nHint: Salary should more than 1000 .. ;)");
                                                    jPanelWrongSalary.setVisible(true);
                                                }
                                            }
                                            else{
                                                JOptionPane.showMessageDialog(null, "Not valid Phone No Try Again :(\nHint: Phone No should  equal 11 numbers if it mobile\n or more than 8 with Code of Governorate if it Tel  .. ;)");
                                                jPanelWrongPhoneNo.setVisible(true);
                                            }
                                        }
                                        else{
                                            JOptionPane.showMessageDialog(null, "Not valid Age Try Again :(\nHint: Age should more than 18 .. ;)");
                                            jPanelWrongAge.setVisible(true);
                                        }
                                    }
                                    else{
                                        JOptionPane.showMessageDialog(null, "Not valid Input for Email Try Again :(");
                                        jPanelWrongEmail.setVisible(true);
                                    }
                            }
                            else{
                                JOptionPane.showMessageDialog(null, "Not valid Password length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n                    Try Again :(");
                                jPanelWrongPass.setVisible(true);
                            }
                    }
                        else{
                            JOptionPane.showMessageDialog(null, "Not valid User Name Try Again :(");
                            jPanelWrongUserName.setVisible(true);
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                        jPanelWrongName.setVisible(true);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                    jPanelWrongID.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Employee Try Again :(\nHint: ID Employee Length should more than 4 .. ;)");
                jPanelWrongIDEmployee.setVisible(true);
            }
        }
        else
            jLabelSuccessOrFail.setText("Missing Required ID Employee ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel18MouseClicked

